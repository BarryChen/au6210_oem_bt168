BOOL NPCA110X_ADC_Input_CH_Select(INPUT_CH ch)
{
	BOOL acktemp = 0;
	BYTE Ch_temp,gBassDSP_CMD[3] = {0xff,0xad,0x83} ;	
	Ch_temp = (((BYTE)ch<<4)&0xf0) | ((BYTE)ch&0x0f);
	acktemp = NPCA110X_IIC_Send_Cmd(gBassDSP_CMD);	
	gBassDSP_CMD[0] = 0x00;	
	gBassDSP_CMD[1] = 0x00;
	gBassDSP_CMD[2] = Ch_temp;
	acktemp |= NPCA110X_IIC_Send_Cmd(gBassDSP_CMD);
	acktemp |= NPCA110X_SendCODE_Latch_CMD();
	WaitMs(2);
	return acktemp;		
}


